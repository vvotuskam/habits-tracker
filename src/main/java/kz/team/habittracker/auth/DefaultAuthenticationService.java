    private final EmailSender emailSender;
    private final ConfirmationTokenService confirmationTokenService;
    private final UserService userService;

    @Override
    public AuthenticationResponse register(RegistrationRequest request) {

        User user = User.builder()
                .firstname(request.getFirstname())
                .lastname(request.getLastname())
                .email(request.getEmail())
                .password(request.getPassword())
                .role(Role.ROLE_USER)
                .build();

        String confirmToken = userService.signUpUser(user);
        String jwtToken = jwtService.generateToken(user);

        saveUserToken(user, jwtToken);
        String link = "http://localhost:8080/api/v1/auth/confirm?token=" + confirmToken;
        emailSender.send(request.getEmail(), buildEmail(request.getFirstname(), link));

        return new AuthenticationResponse(jwtToken);
    }

    @Override
    public AuthenticationResponse authenticate(AuthenticationRequest request) {

        System.out.println("Auth started");

        Authentication auth = authenticationManager.authenticate(
                new UsernamePasswordAuthenticationToken(request.getEmail(), request.getPassword())
        );

        System.out.println(auth);

        User user = userRepository.findByEmail(request.getEmail()).orElseThrow();
        String token = jwtService.generateToken(user);

        revokeAllUserTokens(user);
        saveUserToken(user, token);

        return new AuthenticationResponse(token);
    }

    @Override
    public void saveUserToken(User user, String token) {
        JwtToken jwt = JwtToken.builder()
                .user(user)
                .token(token)
                .expired(false)
                .revoked(false)
                .build();
        jwtTokenRepository.save(jwt);
    }

    @Override
    public void revokeAllUserTokens(User user) {
        List<JwtToken> validUserTokens = jwtTokenRepository.findAllValidTokenByUser(user.getId());
        if (validUserTokens.isEmpty())
            return;
        validUserTokens.forEach(token -> {
            token.setExpired(true);
            token.setRevoked(true);
        });
        jwtTokenRepository.saveAll(validUserTokens);
    }

    @Override
    public String confirmToken(String token) {
        ConfirmationToken confirmationToken = confirmationTokenService
                .getToken(token)
                .orElseThrow(() ->
                        new IllegalStateException("token not found"));

        if (confirmationToken.getConfirmedAt() != null) {
            throw new IllegalStateException("email already confirmed");
        }

        LocalDateTime expiredAt = confirmationToken.getExpiresAt();

        if (expiredAt.isBefore(LocalDateTime.now())) {
            throw new IllegalStateException("token expired");
        }

        confirmationTokenService.setConfirmedAt(token);
        userService.enableUser(confirmationToken.getUser().getEmail());
        return "confirmed";
    }

